    public static String ChatBot(String prompt) throws IOException {
        //modelo
        String modelo = "llama3.2";

        //Conectar con el enlace
        URL url = null;
        try {
            url = new URL("http://localhost:11434/api/generate");
        } catch (MalformedURLException ex) {
            Logger.getLogger(UIOllama.class.getName()).log(Level.SEVERE, null, ex);
        }
        HttpURLConnection conn = (HttpURLConnection) url.openConnection();
        try {
            conn.setRequestMethod("POST");
        } catch (ProtocolException ex) {
            Logger.getLogger(UIOllama.class.getName()).log(Level.SEVERE, null, ex);
        }
        conn.setRequestProperty("Contet-Type", "application/json; utf-8");
        conn.setRequestProperty("Accept", "application/json");
        conn.setDoOutput(true);

        //La generacion del prompt
        String input = String.format("{\"model\": \"%s\",\"prompt\": \"%s\", \"stream\": false}", modelo, prompt);

        try ( OutputStream os = conn.getOutputStream()) {
            byte[] inputjson = input.getBytes(StandardCharsets.UTF_8);
            os.write(inputjson, 0, inputjson.length);
        }

        //Se acomoda el texto con json y se envia la respuesta
        BufferedReader in = new BufferedReader(new InputStreamReader(conn.getInputStream(), StandardCharsets.UTF_8));
        StringBuilder response = new StringBuilder();
        String line;
        while ((line = in.readLine()) != null) {
            response.append(line);
        }
        in.close();

        System.out.println("Response Body: " + response.toString());

        JSONObject jsonresponse = new JSONObject(response.toString());
        String responsetext = jsonresponse.getString("response");

        conn.disconnect();
        return responsetext;
    }

