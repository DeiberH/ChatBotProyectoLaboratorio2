    private void nuevoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nuevoMouseClicked
        // TODO add your handling code here:
        
        hstrl[c][0] = "Chat " + (c + 1);
        for (int i = 0; i < ind.length - 1; i++) {
            if (hstrl[i][0] != null) {
                ind[i] = hstrl[i][0];
                c = i;
            }

        }
        for (int i = 0; i < cht.length - 1; i++) {
            if (cht[i] != null) {
                cht[i] = "";
            }
        }
        c = c + 1;
        
        historial.setListData(ind);
        Chat.setListData(cht);
    }//GEN-LAST:event_nuevoMouseClicked

    private void enviarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_enviarMouseClicked
        // TODO add your handling code here:

        String prpt = prompt.getText();
        String respuesta = null;
        try {
            respuesta = ChatBot(prpt);
        } catch (IOException ex) {
            Logger.getLogger(UIOllama.class.getName()).log(Level.SEVERE, null, ex);
        }
        boolean s = false;
        for (int i = 0; i < cht.length - 1; i += 2) {
            if (cht[i] == null) {
                cht[i] = "Usuario: " + prpt;
                cht[i + 1] = "Ollama: " + respuesta;
                prompt.setText("");
                s = true;
                break;
            }

        }
        for (int i = 0; i < cht.length - 1; i++) {
            if (cht[i] != null) {
                hstrl[c][i + 1] = cht[i];
            }
        }
        if (s) {
            Chat.setListData(cht);
        }


    }//GEN-LAST:event_enviarMouseClicked

